def create_scaffold(dataset_dir: Path, classes: list):
    """Create the empty dataset directory structure and data.yaml."""
    print(f"Creating dataset scaffold at: {dataset_dir}")

    # Create directories
    for split in ["train", "val", "test"]:
        (dataset_dir / split / "images").mkdir(parents=True, exist_ok=True)
        (dataset_dir / split / "labels").mkdir(parents=True, exist_ok=True)

    # Create data.yaml
    yaml_content = generate_data_yaml(dataset_dir, classes)
    yaml_path = dataset_dir / "data.yaml"
    yaml_path.write_text(yaml_content)

    # Create README
    readme = f"""# Traffic Sign Detection Dataset

## Directory Structure
```
traffic_signs/
├── data.yaml          # Dataset configuration
├── train/
│   ├── images/        # Training images (.jpg, .png)
│   └── labels/        # YOLO format labels (.txt)
├── val/
│   ├── images/        # Validation images
│   └── labels/        # Validation labels
└── test/              # (Optional) Test images
    ├── images/
    └── labels/
```

## YOLO Label Format
Each `.txt` file contains one line per object:
```
class_id center_x center_y width height
```
All values are normalized to [0, 1] relative to image dimensions.

Example (speed_limit_60 at center of image):
```
5 0.5 0.5 0.1 0.15
```

## Classes ({len(classes)} total)
{chr(10).join(f'{i}: {c}' for i, c in enumerate(classes))}

## Adding Images
1. Place images in train/images/ (and val/images/ for validation)
2. Create corresponding label files in train/labels/ (and val/labels/)
3. Run `python prepare_dataset.py --verify` to check the dataset

## Recommended Sources
- Roboflow Universe: https://universe.roboflow.com/search?q=traffic+sign
- LISA Dataset: http://cvrr.ucsd.edu/LISA/lisa-traffic-sign-dataset.html
- TT100K: https://cg.cs.tsinghua.edu.cn/traffic-sign/
- CVAT (for custom annotation): https://www.cvat.ai/
"""
    (dataset_dir / "README.md").write_text(readme)

    print(f"  Created directories for train/val/test splits")
    print(f"  Created data.yaml with {len(classes)} classes")
    print(f"  Created README.md")
    print(f"\nNext steps:")
    print(f"  1. Add images to {dataset_dir}/train/images/")
    print(f"  2. Add YOLO labels to {dataset_dir}/train/labels/")
    print(f"  3. Run: python prepare_dataset.py --verify")


def generate_data_yaml(dataset_dir: Path, classes: list) -> str:
    """Generate the data.yaml file content."""
    # Use absolute paths for reliability
    abs_dir = dataset_dir.resolve()

    lines = [
        f"# Traffic Sign Detection Dataset",
        f"# Generated by prepare_dataset.py",
        f"# {len(classes)} classes",
        f"",
        f"path: {abs_dir}",
        f"train: train/images",
        f"val: val/images",
        f"test: test/images",
        f"",
        f"# Number of classes",
        f"nc: {len(classes)}",
        f"",
        f"# Class names",
        f"names:",
    ]
    for i, cls_name in enumerate(classes):
        lines.append(f"  {i}: {cls_name}")

    return "\n".join(lines) + "\n"


def download_roboflow(workspace_url: str, dataset_dir: Path):
    """Download a dataset from Roboflow."""
    try:
        from roboflow import Roboflow
    except ImportError:
        print("ERROR: roboflow not installed. pip install roboflow")
        sys.exit(1)

    api_key = os.environ.get("ROBOFLOW_API_KEY")
    if not api_key:
        print("ERROR: Set ROBOFLOW_API_KEY environment variable")
        print("  Get your API key from: https://app.roboflow.com/settings/api")
        print("  Then: set ROBOFLOW_API_KEY=your_key_here")
        sys.exit(1)

    print(f"Downloading from Roboflow: {workspace_url}")
    print("(This may take a few minutes...)")

    rf = Roboflow(api_key=api_key)

    # Parse workspace/project/version from URL
    # Expected format: workspace/project/version
    parts = workspace_url.strip("/").split("/")
    if len(parts) < 2:
        print("ERROR: URL should be in format: workspace/project or workspace/project/version")
        sys.exit(1)

    workspace = parts[0]
    project_name = parts[1]
    version = int(parts[2]) if len(parts) > 2 else 1

    project = rf.workspace(workspace).project(project_name)
    dataset = project.version(version).download("yolov8", location=str(dataset_dir))

    print(f"  Downloaded to: {dataset_dir}")
    print(f"  Run --verify to check the dataset")


def import_local(source_dir: Path, dataset_dir: Path, split_ratio: tuple = (0.8, 0.1, 0.1)):
    """Import images and labels from a local directory into the dataset structure."""
    source_dir = Path(source_dir)

    # Find images
    img_extensions = {".jpg", ".jpeg", ".png", ".bmp", ".tiff"}
    images = []
    for ext in img_extensions:
        images.extend(source_dir.rglob(f"*{ext}"))
        images.extend(source_dir.rglob(f"*{ext.upper()}"))

    images = sorted(set(images))
    print(f"Found {len(images)} images in {source_dir}")

    if not images:
        print("ERROR: No images found.")
        return

    # Check for labels
    labeled = []
    unlabeled = []
    for img_path in images:
        label_path = img_path.with_suffix(".txt")
        # Also check in a 'labels' subdirectory
        alt_label = img_path.parent.parent / "labels" / (img_path.stem + ".txt")

        if label_path.exists():
            labeled.append((img_path, label_path))
        elif alt_label.exists():
            labeled.append((img_path, alt_label))
        else:
            unlabeled.append(img_path)

    print(f"  Labeled: {len(labeled)}")
    print(f"  Unlabeled: {len(unlabeled)} (will be skipped)")

    if not labeled:
        print("ERROR: No labeled images found. Labels should be .txt files in YOLO format.")
        return

    # Shuffle and split
    random.shuffle(labeled)
    n = len(labeled)
    n_train = int(n * split_ratio[0])
    n_val = int(n * split_ratio[1])

    splits = {
        "train": labeled[:n_train],
        "val": labeled[n_train:n_train + n_val],
        "test": labeled[n_train + n_val:],
    }

    # Copy files
    for split_name, split_data in splits.items():
        img_dir = dataset_dir / split_name / "images"
        lbl_dir = dataset_dir / split_name / "labels"
        img_dir.mkdir(parents=True, exist_ok=True)
        lbl_dir.mkdir(parents=True, exist_ok=True)

        for img_path, lbl_path in split_data:
            shutil.copy2(img_path, img_dir / img_path.name)
            shutil.copy2(lbl_path, lbl_dir / (img_path.stem + ".txt"))

        print(f"  {split_name}: {len(split_data)} images")

    print(f"\nImport complete. Run --verify to validate.")


def merge_datasets(dirs: list, dataset_dir: Path):
    """Merge multiple YOLO-format datasets into one."""
    print(f"Merging {len(dirs)} datasets into {dataset_dir}")

    for split in ["train", "val", "test"]:
        img_dir = dataset_dir / split / "images"
        lbl_dir = dataset_dir / split / "labels"
        img_dir.mkdir(parents=True, exist_ok=True)
        lbl_dir.mkdir(parents=True, exist_ok=True)

        count = 0
        for src_dir in dirs:
            src_dir = Path(src_dir)
            src_img = src_dir / split / "images"
            src_lbl = src_dir / split / "labels"

            if not src_img.exists():
                continue

            for img_file in src_img.iterdir():
                if img_file.suffix.lower() in {".jpg", ".jpeg", ".png"}:
                    # Prefix with source dir name to avoid conflicts
                    prefix = src_dir.name
                    new_name = f"{prefix}_{img_file.name}"

                    shutil.copy2(img_file, img_dir / new_name)

                    lbl_file = src_lbl / (img_file.stem + ".txt")
                    if lbl_file.exists():
                        shutil.copy2(lbl_file, lbl_dir / f"{prefix}_{img_file.stem}.txt")

                    count += 1

        print(f"  {split}: {count} images merged")


def verify_dataset(dataset_dir: Path):
    """Verify the integrity of the dataset."""
    print(f"Verifying dataset at: {dataset_dir}")
    print("=" * 60)

    issues = []
    stats = {}

    yaml_path = dataset_dir / "data.yaml"
    if not yaml_path.exists():
        print(f"❌ data.yaml not found!")
        issues.append("Missing data.yaml")
    else:
        print(f"✓ data.yaml exists")

    for split in ["train", "val", "test"]:
        img_dir = dataset_dir / split / "images"
        lbl_dir = dataset_dir / split / "labels"

        if not img_dir.exists():
            if split != "test":
                issues.append(f"Missing {split}/images/")
                print(f"❌ {split}/images/ not found")
            continue

        images = list(img_dir.glob("*.jpg")) + list(img_dir.glob("*.png"))
        labels = list(lbl_dir.glob("*.txt"))

        image_stems = {p.stem for p in images}
        label_stems = {p.stem for p in labels}

        missing_labels = image_stems - label_stems
        orphan_labels = label_stems - image_stems

        # Analyze label contents
        class_counts = Counter()
        bbox_count = 0
        bad_labels = []

        for lbl_path in labels:
            try:
                with open(lbl_path) as f:
                    for line_num, line in enumerate(f, 1):
                        parts = line.strip().split()
                        if len(parts) != 5:
                            bad_labels.append(f"{lbl_path.name}:{line_num}")
                            continue

                        cls_id = int(parts[0])
                        cx, cy, w, h = [float(x) for x in parts[1:]]

                        class_counts[cls_id] += 1
                        bbox_count += 1

                        # Validate ranges
                        if not (0 <= cx <= 1 and 0 <= cy <= 1 and 0 < w <= 1 and 0 < h <= 1):
                            bad_labels.append(f"{lbl_path.name}:{line_num} (out of range)")

            except (ValueError, IndexError) as e:
                bad_labels.append(f"{lbl_path.name} (parse error: {e})")

        stats[split] = {
            "images": len(images),
            "labels": len(labels),
            "bboxes": bbox_count,
            "classes": dict(class_counts),
        }

        # Report
        status = "✓" if not missing_labels and not bad_labels else "⚠️"
        print(f"\n{status} {split}:")
        print(f"    Images: {len(images)}")
        print(f"    Labels: {len(labels)}")
        print(f"    Bboxes: {bbox_count}")
        print(f"    Classes used: {sorted(class_counts.keys())}")

        if missing_labels:
            print(f"    ⚠️  {len(missing_labels)} images without labels")
            if len(missing_labels) <= 5:
                for m in missing_labels:
                    print(f"       - {m}")
            issues.append(f"{split}: {len(missing_labels)} images missing labels")

        if orphan_labels:
            print(f"    ⚠️  {len(orphan_labels)} orphan labels (no matching image)")

        if bad_labels:
            print(f"    ❌ {len(bad_labels)} malformed labels")
            for b in bad_labels[:5]:
                print(f"       - {b}")
            issues.append(f"{split}: {len(bad_labels)} bad labels")

        # Class distribution
        if class_counts:
            print(f"    Class distribution:")
            for cls_id in sorted(class_counts.keys()):
                bar_len = int(class_counts[cls_id] / max(class_counts.values()) * 30)
                bar = "█" * bar_len
                print(f"      {cls_id:3d}: {class_counts[cls_id]:6d} {bar}")

    # Summary
    print(f"\n{'='*60}")
    if issues:
        print(f"⚠️  Found {len(issues)} issue(s):")
        for issue in issues:
            print(f"  - {issue}")
    else:
        print(f"✅ Dataset looks good!")

    # Recommendations
    train_count = stats.get("train", {}).get("images", 0)
    val_count = stats.get("val", {}).get("images", 0)

    if train_count < 500:
        print(f"\n💡 Tip: {train_count} training images is low.")
        print(f"   Aim for 1000+ images for reliable detection.")
        print(f"   Consider downloading more from Roboflow Universe.")

    if val_count < 50:
        print(f"\n💡 Tip: {val_count} validation images is very low.")
        print(f"   Aim for at least 100 validation images.")

    return len(issues) == 0


def sample_preview(dataset_dir: Path, num_samples: int = 9):
    """Show a grid of sample images with their annotations drawn."""
    print(f"Generating sample preview...")

    img_dir = dataset_dir / "train" / "images"
    lbl_dir = dataset_dir / "train" / "labels"

    if not img_dir.exists():
        print("No training images found.")
        return

    images = list(img_dir.glob("*.jpg")) + list(img_dir.glob("*.png"))
    if not images:
        print("No images found.")
        return

    samples = random.sample(images, min(num_samples, len(images)))

    # Create grid
    cols = 3
    rows = (len(samples) + cols - 1) // cols
    cell_size = 300

    grid = np.zeros((rows * cell_size, cols * cell_size, 3), dtype=np.uint8)

    colors = [(0, 255, 0), (255, 0, 0), (0, 0, 255), (255, 255, 0),
              (255, 0, 255), (0, 255, 255), (128, 255, 0), (255, 128, 0)]

    for idx, img_path in enumerate(samples):
        row, col = idx // cols, idx % cols

        img = cv2.imread(str(img_path))
        if img is None:
            continue

        h, w = img.shape[:2]

        # Draw annotations
        lbl_path = lbl_dir / (img_path.stem + ".txt")
        if lbl_path.exists():
            with open(lbl_path) as f:
                for line in f:
                    parts = line.strip().split()
                    if len(parts) != 5:
                        continue
                    cls_id = int(parts[0])
                    cx, cy, bw, bh = [float(x) for x in parts[1:]]

                    x1 = int((cx - bw / 2) * w)
                    y1 = int((cy - bh / 2) * h)
                    x2 = int((cx + bw / 2) * w)
                    y2 = int((cy + bh / 2) * h)

                    color = colors[cls_id % len(colors)]
                    cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
                    cv2.putText(img, str(cls_id), (x1, y1 - 5),
                                cv2.FONT_HERSHEY_SIMPLEX, 0.5, color, 1)

        # Resize and place in grid
        img_resized = cv2.resize(img, (cell_size, cell_size))
        grid[row * cell_size:(row + 1) * cell_size,
             col * cell_size:(col + 1) * cell_size] = img_resized

    # Save preview
    preview_path = dataset_dir / "preview.png"
    cv2.imwrite(str(preview_path), grid)
    print(f"  Preview saved to: {preview_path}")

    # Also show if display is available
    try:
        cv2.imshow("Dataset Preview", grid)
        print("  Press any key to close preview...")
        cv2.waitKey(0)
        cv2.destroyAllWindows()
    except cv2.error:
        pass  # No display available


def main():
    parser = argparse.ArgumentParser(description="Prepare Street Sign Detection Dataset")

    # Actions
    parser.add_argument("--scaffold", action="store_true",
                        help="Create empty dataset structure")
    parser.add_argument("--from-roboflow", type=str, default=None,
                        help="Download from Roboflow (workspace/project/version)")
    parser.add_argument("--from-local", type=str, default=None,
                        help="Import from local directory with images+labels")
    parser.add_argument("--merge", nargs="+",
                        help="Merge multiple dataset directories")
    parser.add_argument("--verify", action="store_true",
                        help="Verify dataset integrity")
    parser.add_argument("--preview", action="store_true",
                        help="Show sample images with annotations")

    # Options
    parser.add_argument("--output", type=str, default=str(DATASET_DIR),
                        help=f"Output directory (default: {DATASET_DIR})")
    parser.add_argument("--split", type=str, default="0.8,0.1,0.1",
                        help="Train/val/test split ratio (default: 0.8,0.1,0.1)")

    args = parser.parse_args()
    dataset_dir = Path(args.output)

    if args.scaffold:
        create_scaffold(dataset_dir, DEFAULT_CLASSES)
    elif args.from_roboflow:
        download_roboflow(args.from_roboflow, dataset_dir)
    elif args.from_local:
        split_ratio = tuple(float(x) for x in args.split.split(","))
        import_local(Path(args.from_local), dataset_dir, split_ratio)
    elif args.merge:
        merge_datasets(args.merge, dataset_dir)
    elif args.verify:
        verify_dataset(dataset_dir)
    elif args.preview:
        sample_preview(dataset_dir)
    else:
        parser.print_help()
        print("\n\nQuick Start:")
        print("  1. Create structure:  python prepare_dataset.py --scaffold")
        print("  2. Add your images and labels to datasets/traffic_signs/train/")
        print("  3. Verify:            python prepare_dataset.py --verify")
        print("  4. Preview:           python prepare_dataset.py --preview")
        print("  5. Train:             python train_sign_detector.py")


if __name__ == "__main__":
    main()
